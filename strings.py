greetings = "Hello, How are you doing?"
print(greetings)

name = "Hassan"

# Formatted strings
print(f'How are you doing [{name}]')

age = 30
print(f'I am {age} years old')

# Getting the first character
print(name[0])

# Getting the last charcter
print(name[-1])

# Charcters between indeces
print(name[1:-1])

# Charcters the begin from a certain index
print(name[1:])


# STRING METHODS
# len
print(len(name))

# To uppercase
print(greetings.upper())

# To lowercase
print(greetings.lower())

# Capitalize the first letter
print(greetings.title())

